def check_dependencies():
    missing = []
    try:
        import rich
    except ImportError:
        missing.append("rich")
    try:
        import dotenv
    except ImportError:
        pass

    if missing:
        print(f"Missing required dependencies: {', '.join(missing)}")
        print(f"Run: pip install {' '.join(missing)}")
        sys.exit(1)


