def run_interactive(agent, config):
    from agent.display import (
        print_banner, print_help, prompt_user, clear_screen,
        print_success, print_error, print_info, print_warning,
        print_separator, print_status_table, print_diff, console, COLORS
    )
    from agent.context import ConversationContext
    from providers.factory import get_all_provider_info
    from pathlib import Path
    import readline
    readline.set_history_length(1000)

    print_banner(config.provider, config.model)

    while True:
        try:
            user_input = prompt_user()
        except (KeyboardInterrupt, EOFError):
            print_warning("\nUse /exit to quit.")
            continue

        user_input = user_input.strip()
        if not user_input:
            continue

        if user_input.startswith("/"):
            parts = user_input.split(None, 2)
            cmd = parts[0].lower()
            args = parts[1] if len(parts) > 1 else ""
            rest = parts[2] if len(parts) > 2 else ""

            if cmd in ("/exit", "/quit", "/q"):
                print_info("Goodbye!")
                break

            elif cmd == "/help":
                print_help()

            elif cmd == "/clear":
                clear_screen()
                agent.context.reset()
                print_banner(config.provider, config.model)

            elif cmd == "/reset":
                agent.context.reset()
                print_success("Conversation history cleared.")

            elif cmd == "/provider":
                if not args:
                    info = get_all_provider_info()
                    for k, v in info.items():
                        mark = "★" if k == config.provider else " "
                        console.print(f"  {mark} [{COLORS['tool']}]{k}[/] — {v['models'][0]}")
                else:
                    model_override = rest if rest else ""
                    if agent.switch_provider(args, model_override):
                        agent.context.reset()

            elif cmd == "/model":
                if not args:
                    if hasattr(agent.provider, 'list_models'):
                        models = agent.provider.list_models()
                        console.print(f"Available models for {config.provider}:")
                        for m in models:
                            mark = "★" if m == config.model else " "
                            console.print(f"  {mark} {m}")
                    else:
                        print_info(f"Current model: {config.model}")
                else:
                    config.model = args
                    agent._load_provider()
                    print_success(f"Switched to model: {args}")

            elif cmd == "/models":
                if hasattr(agent.provider, "list_models"):
                    models = agent.provider.list_models()
                    for m in models:
                        console.print(f"  • {m}")

            elif cmd == "/providers":
                from main import show_providers
                show_providers()

            elif cmd == "/status":
                print_status_table(agent.status)

            elif cmd == "/history":
                console.print(agent.context.format_history())

            elif cmd == "/compact":
                result = agent.compact_history()
                print_info(result)

            elif cmd == "/cost":
                stats = agent.context.get_stats()
                print_status_table(stats)

            elif cmd == "/verbose":
                agent.verbose = not agent.verbose
                config.verbose = agent.verbose
                state = "ON" if agent.verbose else "OFF"
                print_info(f"Verbose mode: {state}")

            elif cmd == "/safe":
                agent.safe_mode = not agent.safe_mode
                config.safe_mode = agent.safe_mode
                state = "ON" if agent.safe_mode else "OFF"
                print_info(f"Safe mode: {state}")

            elif cmd == "/cd":
                if not args:
                    print_info(f"Current directory: {agent.context.workspace}")
                else:
                    new_dir = Path(agent.context.workspace) / args
                    try:
                        new_dir = new_dir.resolve()
                        if new_dir.is_dir():
                            agent.context.workspace = str(new_dir)
                            os.chdir(new_dir)
                            print_success(f"Changed directory to: {new_dir}")
                        else:
                            print_error(f"Directory not found: {args}")
                    except Exception as e:
                        print_error(str(e))

            elif cmd == "/pwd":
                print_info(f"Working directory: {agent.context.workspace}")

            elif cmd == "/ls":
                from tools.file_tools import list_directory
                result = list_directory(args or ".", workspace=agent.context.workspace)
                console.print(result)

            elif cmd == "/run":
                if args:
                    from tools.bash_tool import run_command
                    result = run_command(f"{args} {rest}".strip(), workspace=agent.context.workspace)
                    console.print(result)

            elif cmd == "/save":
                if args:
                    path = Path(args)
                    agent.context.save(path)
                    print_success(f"Conversation saved to {args}")
                else:
                    print_error("Usage: /save <filename>")

            elif cmd == "/load":
                if args:
                    path = Path(args)
                    if path.exists():
                        agent.context.load(path)
                        print_success(f"Loaded conversation from {args}")
                    else:
                        print_error(f"File not found: {args}")

            elif cmd == "/undo":
                op = agent.context.pop_undo()
                if op:
                    print_info(f"Undo: {op}")
                else:
                    print_info("Nothing to undo.")

            elif cmd == "/init":
                from tools.utility_tools import init_project
                ptype = args or "python"
                name = rest or Path(agent.context.workspace).name
                result = init_project(ptype, name, agent.context.workspace)
                console.print(result)

            elif cmd == "/memory":
                stats = agent.context.get_stats()
                print_status_table(stats)
                console.print(f"\n[{COLORS['dim']}]Tool categories: {list(registry.list_by_category().keys())}[/]")

            else:
                print_warning(f"Unknown command: {cmd}. Type /help for help.")
        else:
            try:
                agent.run(user_input)
            except KeyboardInterrupt:
                print_warning("\nTask interrupted. Use /exit to quit.")
            except Exception as e:
                print_error(f"Agent error: {e}")
                if config.verbose:
                    import traceback
                    traceback.print_exc()


def main():
    check_dependencies()
    args = parse_args()

    if args.providers:
        show_providers()
        return

    if args.setup:
        setup_wizard()
        return

    try:
        from dotenv import load_dotenv
        load_dotenv()
    except ImportError:
        pass

    from agent.config import Config
    config = Config()

    if args.provider:
        config.provider = args.provider.lower()
        config.model = config.get_default_model(config.provider)
    if args.model:
        config.model = args.model
    if args.safe:
        config.safe_mode = True
    if args.verbose:
        config.verbose = True
    if args.no_stream:
        config.stream = False
    if args.max_iterations:
        config.max_iterations = args.max_iterations

    workspace = str(Path(args.workspace).resolve())
    if not Path(workspace).is_dir():
        print(f"Error: Workspace directory not found: {workspace}")
        sys.exit(1)

    os.chdir(workspace)

    from agent.context import ConversationContext
    context = ConversationContext(workspace=workspace)

    register_all_tools(lambda: context.workspace)

    from agent.core import Agent
    from tools.registry import registry

    agent = Agent(config, context)

    if args.task:
        from agent.display import print_user_message
        print_user_message(args.task)
        result = agent.run(args.task)
        if result:
            print(result)
        return

    run_interactive(agent, config)


if __name__ == "__main__":
    main()







