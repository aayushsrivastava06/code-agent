def setup_wizard():
    from agent.display import console, COLORS, print_info, print_success, print_warning
    from rich.prompt import Prompt, Confirm
    from providers.factory import get_all_provider_info

    console.print(f"\n[bold {COLORS['primary']}]Code Agent Setup Wizard[/]\n")
    info = get_all_provider_info()

    env_path = Path(".env")
    existing = {}
    if env_path.exists():
        for line in env_path.read_text().splitlines():
            if "=" in line and not line.startswith("#"):
                k, _, v = line.partition("=")
                existing[k.strip()] = v.strip()

    lines = ["# Code Agent Configuration", "# Generated by setup wizard", ""]
    
    print_info("Choose your primary AI provider:")
    for i, (k, v) in enumerate(info.items()):
        note = f"  — {v['notes'][:50]}"
        console.print(f"  [{i+1}] {k:15s} {note}")

    console.print()
    choice_str = Prompt.ask("Enter number (1-8)", default="1")
    try:
        choice_idx = int(choice_str) - 1
        chosen = list(info.keys())[max(0, min(choice_idx, len(info)-1))]
    except Exception:
        chosen = "gemini"

    lines.append(f"AI_PROVIDER={chosen}")
    lines.append(f"AI_MODEL={info[chosen]['models'][0]}")
    lines.append("")

    console.print(f"\n[{COLORS['success']}]Setting up {chosen}...[/]")
    if info[chosen]["key_env"]:
        console.print(f"Get your free API key: [{COLORS['info']}]{info[chosen]['get_key']}[/]")
        key_val = existing.get(info[chosen]["key_env"], "")
        if key_val:
            use_existing = Confirm.ask(f"Found existing {info[chosen]['key_env']}. Keep it?", default=True)
            if not use_existing:
                key_val = Prompt.ask(f"Enter {info[chosen]['key_env']}", password=True)
        else:
            key_val = Prompt.ask(f"Enter {info[chosen]['key_env']} (or press Enter to skip)", default="", password=True)
        if key_val:
            lines.append(f"{info[chosen]['key_env']}={key_val}")

    lines.append("\n# Optional: Set keys for other providers to switch between them")
    for k, v in info.items():
        if k != chosen and v["key_env"]:
            lines.append(f"# {v['key_env']}=your_key_here")

    lines.extend([
        "\n# Agent settings",
        "MAX_ITERATIONS=50",
        "BASH_TIMEOUT=30",
        "# SAFE_MODE=true   # Uncomment to require confirmation before file writes",
        "# VERBOSE=true     # Uncomment for detailed tool output",
    ])

    env_path.write_text("\n".join(lines))
    print_success(f"Configuration saved to .env")
    print_info(f"Run 'python main.py' to start the agent.")


